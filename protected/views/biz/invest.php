						<div class=" fs14  ml15  text-omit small-title font-hua" style="color:#999; width:600px; font-style:italic;font-weight:bold;">总资产周转率+资产负债率+流动比率+应收帐款周转天数（应收帐款周转率）+销售毛利率+净值报酬率+营活动净现金流+市场占有率

						

